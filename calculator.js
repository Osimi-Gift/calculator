var opr = prompt (" what Operator do you want to perform e.g '+' '-' '/' '*' ")
var num1 = parseInt(prompt("Number 1: "))
var num2 =parseInt(prompt("Number 2: "))



if(opr == "+" )
{
    alert( num1 + num2)  
}  else if(opr == "-")
{
    alert(num1 - num2 )
} else if(opr == "*")
{
    alert(num1 * num2  )
} else if(opr == "/")
{
    alert(num1 / num2  )
}